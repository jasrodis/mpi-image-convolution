/******************************************************************************
 * NAME:
 *  util/log.h
 * DESCRIPTION:
 *  Logging header file.
 *****************************************************************************/
#ifndef _UTIL_LOG
#define _UTIL_LOG

#include <stdio.h>

/******************************************************************************
 * Log levels
 *****************************************************************************/

typedef enum {
    LOG_DEBUG = 0,
    LOG_INFO = 1,
    LOG_WARNING = 2,
    LOG_ERROR = 3
} LogLevel;

/******************************************************************************
 * Log level management
 *****************************************************************************/

void log_setLogLevel(LogLevel level);

/******************************************************************************
 * Log stream
 *****************************************************************************/

void log_setLogStream(FILE *stream);

/******************************************************************************
 * Log events
 *****************************************************************************/

void log_log(LogLevel level, const char *msg, ...);

#endif
